	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=0XFF
	LDR R1,=0X0A
	
	LDR R0,=NUM
	MOV R1,#0X0A
UP
	CMP R0,#0X0A
	BLCS DIV
	ORR R4,R0
	ROR R4,#4
	MOV R0,R2
	B UP
STOP B STOP
	AREA mydata, DATA, READWRITE
DST DCD 0
	END